package tags;

import javax.servlet.jsp.JspWriter;
import javax.servlet.jsp.JspException;
import javax.servlet.jsp.tagext.JspFragment;
import javax.servlet.jsp.tagext.SimpleTagSupport;

/**
 *
 * @author ASUS
 */
public class Pager extends SimpleTagSupport {

    private int pageIndex;
    private int pageSize;

    /**
     * Called by the container to invoke this tag. The implementation of this
     * method is provided by the tag library developer, and handles all tag
     * processing, body iteration, etc.
     */
    @Override
    public void doTag() throws JspException {
        JspWriter out = getJspContext().getOut();
        
        try {
            out.print("<a href=search?pageIdx="+pageIndex+"> First </a>");
            for(int i = pageIndex;i<=(pageIndex+pageSize);i++){
                out.print("<a href=search?pageIdx="+i+"> "+i+" </a>");
            }
            out.print("<a href=search?pageIdx="+(pageIndex+pageSize)+"> Last </a>");
            JspFragment f = getJspBody();
            if (f != null) {
                f.invoke(out);
            }

            // TODO: insert code to write html after writing the body content.
            // e.g.:
            //
            // out.println("    </blockquote>");
        } catch (java.io.IOException ex) {
            throw new JspException("Error in Pager tag", ex);
        }
    }

    public void setPageIndex(int pageIndex) {
        this.pageIndex = pageIndex;
    }

    public void setPageSize(int pageSize) {
        this.pageSize = pageSize;
    }
    
}
